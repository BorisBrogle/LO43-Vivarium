package fr.utbm.tools;

public class Vector2f {
	
	private float x=0f;
	private float y=0f;
	
	public Vector2f(float x, float y){
		this.x = x;
		this.y = y;
	}
	
	
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
	
}
